export class AppComponent implements OnInit {
  private authService = inject(AuthService);
  private moduleAccessService = inject(ModuleAccessService);
  private destroyRef = inject(DestroyRef);
  moduleAccess = signal<ModuleAccess[]>([]);
  isAuthenticated = computed(() => this.authService.isAuthenticated());
  isSidebarCollapsed = typeof window !== 'undefined' && window.innerWidth < 1024;
  theme = 'light';
  userName = computed(() => {
    this.authService.isAuthenticated();
    return this.authService.getUser()?.nombres ?? 'Usuario';
  });
  userRole = computed(() => {
    this.authService.isAuthenticated();
    return this.authService.getUser()?.rol ?? 'Rol';
  });

  constructor() {
    effect(() => {
      if (this.isAuthenticated()) {
        this.loadModules();
      } else {
        this.moduleAccess.set([]);
      }
    });
  }

  /**
   * Cierra la sesión del usuario.
   */
  logout() {
    this.authService.logout();
  }

  /**
   * Alterna el estado de la barra lateral.
   */
  toggleSidebar() {
    this.isSidebarCollapsed = !this.isSidebarCollapsed;
  }

  ngOnInit() {
    const stored = typeof window !== 'undefined' ? window.localStorage.getItem('pickeat_theme') : null;
    this.theme = stored === 'dark' ? 'dark' : 'light';
    this.applyTheme();
  }

  toggleTheme() {
    this.theme = this.theme === 'dark' ? 'light' : 'dark';
    if (typeof window !== 'undefined') {
      window.localStorage.setItem('pickeat_theme', this.theme);
    }
    this.applyTheme();
  }

  private applyTheme() {
    if (typeof document === 'undefined') {
      return;
    }
    document.body.classList.toggle('theme-dark', this.theme === 'dark');
  }

  /**
   * Cierra el menú en pantallas pequeñas al interactuar con el contenido.
   */
  closeSidebarOnMobile() {
    if (typeof window !== 'undefined' && window.innerWidth < 1024 && !this.isSidebarCollapsed) {
      this.isSidebarCollapsed = true;
    }
  }

  /**
   * Obtiene las iniciales del usuario autenticado para el avatar.
   */
  userInitials() {
    const name = this.userName();
    return name
      .split(' ')
      .filter(Boolean)
      .slice(0, 2)
      .map((part) => part[0])
      .join('')
      .toUpperCase();
  }

  /**
   * Indica si un módulo está habilitado para el rol actual.
   *
   * @param key llave del módulo.
   */
  hasModule(key: string): boolean {
    const modules = this.moduleAccess();
    if (modules.length === 0) {
      return true;
    }
    return modules.some((module) => module.key === key && module.enabled);
  }

  private loadModules() {
    this.moduleAccessService
      .getModulesForCurrentRole()
      .pipe(takeUntilDestroyed(this.destroyRef))
      .subscribe({
        next: (modules) => this.moduleAccess.set(modules),
        error: () => this.moduleAccess.set([])
      });
  }
}
